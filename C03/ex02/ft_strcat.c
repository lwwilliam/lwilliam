char	*ft_strcat(char *dest, char *src)
{
	int	x;
	int	y;

	y = 0;
	x = 0;
	while (dest[x] != '\0')
		x++;
	while (src[y] != '\0')
	{
		dest[x] = src[y];
		y++;
		x++;
	}
	dest[x] = '\0';
	return (dest);
}
