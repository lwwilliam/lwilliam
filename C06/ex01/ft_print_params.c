#include<unistd.h>

int	main(int ac, char **av)
{
	int	x;
	int	y;

	x = 1;
	while (x <= ac - 1)
	{
		y = 0;
		while (av[x][y] != '\0')
		{
			write(1, &av[x][y], 1);
			y++;
		}
		write(1, "\n", 1);
		x++;
	}
}
